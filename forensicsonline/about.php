        <h1>Audio Forensics</h1><br/>
        Crime is growing at an alarming rate, and we often find ourselves ill-equiped to handle the numerous cases. In instances of terrorist threat calls and false alarms, It is imperative to extract as much information possible from the available audio clippings. 
        With the fast development of technology, it is time we start exploring other methods of identification, apart from fingerprints and images, which would make identification of criminals quick and accurate.Voiceprint of a person is as unique as a fingerprint. 
        Reliable estimates of physical features and demographics can be made from the voice sample even if the person's voiceprint is unavailable to us. There has been little exploration in the area of audio forensics, although we have advanced methods to process voice. Our aim is to build a voiceprint database of the citizens of our country. Law enforcement agencies can make use of this technology to identify criminals based on their voice samples.
        <h3><p>We aim at performing the following : <br/></p></h3>
          1. Find the height, weight and age from the audio sample given as input.<br/>
          2. Investigate the relationship with additional features of the body like circumference of hip, waist and chest, waist-to-hip ratio, chest-to-hip ratio, and body mass index<br/>
          3. Trying to find the geographic location (based on accents?) from the audio sample<br/>
          4. The voice-body size correlation may be influenced by other factors such as age, sex, geographical location of the recording, study year, type of speech stimulus measured and the sample size.<br/>
          5. Determine the minimum length of the audio sample required for predicting the features of person.<br/>
